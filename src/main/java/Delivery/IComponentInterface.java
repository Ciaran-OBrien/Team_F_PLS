package Delivery;

/**
 *
 * @authors Luke & Tom
 */
public interface IComponentInterface {
    /**
     * This method will return the Delivery ID
     * @return int Delivery ID
     */
    public int getID();
    
    /**
     * This method will return the parcel ID 
     * @return int Parcel ID
     */
    public int getParcelID();
    
    /**
     * This method will return the courier ID
     * @return int Courier ID
     */
    public int getCourierID();
    
    /**
         * This method will return the delivery ID and the date as a string
         * @return string This will be the ID number
    */
    public String toString();
    
}
